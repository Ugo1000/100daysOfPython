
# Caculator class

class Caculator:
    def __init__(self, x, y):
        self.x = x
        self.y = y

    def get_sum(self):
        return self.x + self.y

    def get_diffrence(self):
        return self.x - self.y

    def get_product(self):
        return self.x * self.y

    def get_quotient(self):
        return self.x / self.y


def do_something(value):
    return value + 1


def to_upper(value):
    return value.upper()


def to_lower(value):
    return value.lower()


def main():
    data = do_something(3)
    print(data)

    upper = to_upper("samuel")
    print(upper)

    lower = to_lower("SAMUEL")
    print(lower)


if __name__ == '__main__':
    main()


