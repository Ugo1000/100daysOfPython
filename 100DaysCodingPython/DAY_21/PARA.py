# Parameters :
# they are object we pass when we define the function.

def say_hello(name ,emoji):
    print(f'good morning {name}, {emoji}')


# Arguments :
# are actual values we provide to a functions when we call/invoke the funtions 
say_hello("samuel","(**)")

# Positional Arguments
say_hello("s-james","(**)")
say_hello("faith","(**)")
say_hello('o)',"phlip")

# Keywords Arguments
say_hello(emoji= ")0", name="Kate")

